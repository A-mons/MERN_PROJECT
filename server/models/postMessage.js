import mongoose from 'mongoose';

////////////////Creating Post Schema Model////////////////

const postSchema = mongoose.Schema({
    title: String,
    message: String,
    creator: String,
    tags: [String],
    selectedFile: String,
    likeCount: {
        type: Number,
        default: 0
    },
    createdAt: {
        type: Date,
        default: new Date()
    },
});

const postMessage = mongoose.model('postMessage', postSchema);
export default postMessage;